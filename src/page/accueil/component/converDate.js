export const daytoString = (dayIndex) => {
  switch (dayIndex) {
    case 1: {
      return "Lun";
    }
    case 2: {
      return "Mar";
    }
    case 3: {
      return "Mer";
    }
    case 4: {
      return "Jeu";
    }
    case 5: {
      return "Ven";
    }
    case 6: {
      return "Sam";
    }
    case 7: {
      return "Dim";
    }
  }
};
export const MonthToString = (MonthIndex) => {
  switch (MonthIndex) {
    case 0: {
      return "Jan";
    }
    case 1: {
      return "Fev";
    }
    case 2: {
      return "Mar";
    }
    case 3: {
      return "Avr";
    }
    case 4: {
      return "Mai";
    }
    case 5: {
      return "Jun";
    }
    case 6: {
      return "Jul";
    }
    case 7: {
      return "Aou";
    }
    case 8: {
      return "Sept";
    }
    case 9: {
      return "Oct";
    }
    case 10: {
      return "Nov";
    }
    case 11: {
      return "Dec";
    }
  }
};
